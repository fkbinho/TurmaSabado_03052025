package operadores;

public class OperadorUnario {
	public static void main(String[] args) {
		
		int x = 1;

		x++;
		
		System.out.println(x++);
		++x;
		System.out.println(x);
		x++;
		System.out.println(++x);
		
		int y = 3;
		
		System.out.println(--y);
		System.out.println(y);
		System.out.println(y--);
		System.out.println(y);
		
		
	}
}
