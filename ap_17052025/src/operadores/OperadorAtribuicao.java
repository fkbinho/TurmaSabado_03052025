package operadores;

public class OperadorAtribuicao {

	public static void main(String[] args) {
		
		int numero = 10;
		
		// numero = numero + 5;
		numero += 5;
		
		// numero = numero - 3;
		numero -= 3;
		
		// numero = numero * 2;
		numero *= 2;
		
		// numero = numero / 3;
		numero /= 3;
		
		// numero = numero % 3;
		numero %= 3;
		
		System.out.println(numero);

	}

}
