package tipos_de_dados_variaveis;

// Comentário de uma linha

/* Bloco de comentário */

public class TipoDeDados {

	// Método Principal
	public static void main(String[] args) {
		
		// Tipo de Dados Primitivos
	
		/* Tipos Numéricos
		 * byte
		 * short
		 * int
		 * long
		 * float
		 * double
		 * */
		byte numero = 127;
		short numero2 = 32767;
		int numero3 = 5684815;		
		long numero4 = 1351541205;
		
		System.out.println(numero3);
		
		// Tipo Numérico com casas decimais
		float numero5 = 1.78f;
		double numero6 = 1.69;
		
		System.out.println(numero5);

		//Tipo Textual
		char sexo = 'M';
		System.out.println(sexo);
		
		// Tipo de dados NÃO PRIMITIVO
		String nome = "Kleber";
		
		boolean eManha = true; // ou  false
		System.out.println(eManha);
		
		
	}

}
